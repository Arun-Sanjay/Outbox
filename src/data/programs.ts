import type { TrainingProgram, ProgramDay } from "../types";

function day(
  dayNumber: number, title: string, description: string,
  sessionType: TrainingProgram["days"][0]["sessionType"],
  suggestedDuration: number, suggestedRounds: number | null,
  suggestedIntensity: TrainingProgram["days"][0]["suggestedIntensity"],
  isRestDay = false
): ProgramDay {
  return {
    dayNumber, title, description, sessionType, suggestedDuration,
    suggestedRounds, suggestedIntensity, comboSetIds: null,
    exercises: null, notes: "", isRestDay,
  };
}

function restDay(dayNumber: number): ProgramDay {
  return day(dayNumber, "Rest Day", "Recovery and active rest. Light stretching, walk, or foam rolling.", "conditioning", 0, null, "light", true);
}

